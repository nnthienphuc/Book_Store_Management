    public static String ChiTietQuyen()
    {   
        String chitietquyen = "";
        
        if(checkboxBH.getState())
        {
            chitietquyen += "qlBanHang";
        }
        if(checkboxNH.getState())
        {
            chitietquyen += "qlNhapHang";
        }
        if(checkboxSP.getState())
        {
            chitietquyen += comboSP.getSelectedIndex() == 0 ? "xemSach" : "qlSach";
        }
        if(checkboxHD.getState())
        {
            chitietquyen += comboHD.getSelectedIndex() == 0 ? "xemHoaDon" : "qlHoaDon";
        }
        if(checkboxKM.getState())
        {
            chitietquyen += comboKM.getSelectedIndex() == 0 ? "xemKhuyenMai" : "qlKhuyenMai";
        }
        if(checkboxNV.getState())
        {
            chitietquyen += comboNV.getSelectedIndex() == 0 ? "xemNhanVien" : "qlNhanVien";
        }
        if(checkboxKH.getState())
        {
            chitietquyen += comboKH.getSelectedIndex() == 0 ? "xemKhachHang" : "qlKhachHang";
        }
        if(checkboxPN.getState())
        {
            chitietquyen += comboPN.getSelectedIndex() == 0 ? "xemPhieuNhap" : "qlPhieuNhap";
        }
        if(checkboxNCC.getState())
        {
            chitietquyen += comboNCC.getSelectedIndex() == 0 ? "xemNCC" : "qlNCC";
        }
        if(checkboxTK.getState())
        {
            chitietquyen += comboTK.getSelectedIndex() == 0 ? "xemTaiKhoan" : "qlTaiKhoan";
        }
        if(checkboxQuyen.getState())
        {
            chitietquyen += comboQuyen.getSelectedIndex() == 0 ? "xemQuyen" : "qlQuyen";
        }
        
        return chitietquyen;
    }
