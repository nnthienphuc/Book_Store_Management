    public void GetInput(){
        String maNCC = "";
               maNCC  =  (String)NCCTable.getValueAt(NCCTable.getSelectedRow(), 1);
        if(maNCC == "")
        {
        JOptionPane.showMessageDialog(null, "Chưa chọn nhà cung cấp nào để sửa");
        }
        else
        {
        SuaNCC(maNCC);
        }
    }
    
    public static void SuaNCC (String maNCC)
    {   NhaCungCap nccSua = null;
        QuanLyNhaCungCapBUS qlnccBUS = new QuanLyNhaCungCapBUS();
        for (NhaCungCap ncc : qlnccBUS.getDsncc()) {
                if (ncc.getMaNCC().equals(maNCC)) {
                    nccSua = ncc;
                }
            }
//            if (spSua == null) {
//                JOptionPane.showMessageDialog(null, "Lỗi, không tìm thấy sản phẩm");
//                this.dispose();
//            }

            
            txtMaNCC.setText(nccSua.getMaNCC());
            txtTenNCC.setText(nccSua.getTenNCC());
            txtDiaChi.setText(nccSua.getDiaChi());
            txtSDT.setText(String.valueOf(nccSua.getSDT()));
            txtFax.setText(String.valueOf(nccSua.getFAX()));
           
    }
    
    public static void XoaNCC(String mancc){
        QuanLyNhaCungCapBUS qlnccBUS = new QuanLyNhaCungCapBUS();
           
                if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn nhà cung cấp " + mancc + " ?", 
                        "Chú ý", JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                    qlnccBUS.delete(mancc);
                   
                
            }
    }
    
